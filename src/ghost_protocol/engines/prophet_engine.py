    def _update_meta_cognition(self, prediction: Optional[Prediction], domain: str):
        """Update meta-cognition data for self-improvement."""
        if prediction:
            self.meta_cognition_data['total_predictions'] += 1
            if domain not in self.meta_cognition_data['domain_performance']:
                self.meta_cognition_data['domain_performance'][domain] = []
            self.meta_cognition_data['domain_performance'][domain].append(prediction.confidence)

    def get_prophecy_status(self) -> Dict[str, Any]:
        """Get current status of the Prophet Engine."""
        return {
            'active_agents': len([a for a in self.agents if a.active]),
            'total_predictions': self.meta_cognition_data['total_predictions'],
            'domain_performance': self.meta_cognition_data['domain_performance'],
            'agent_status': {a.name: {'active': a.active, 'avg_accuracy': sum(a.accuracy_history)/len(a.accuracy_history) if a.accuracy_history else 0} for a in self.agents}
        }


